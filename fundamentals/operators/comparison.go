package main

import "fmt"

func main() {
	var num1 int = 50
	var num2 int = 60

	fmt.Println(num1 == num2) // == equal
	fmt.Println(num1 != num2) // != not equal
	fmt.Println(num1 < num2)  // < less
	fmt.Println(num1 <= num2) // <= less or equal=
	fmt.Println(num1 > num2)  // >     greater
	fmt.Println(num1 >= num2) // >=    greater or equal

}
